DROP TABLE IF EXISTS REPORTER;
CREATE TABLE REPORTER (
ID INT NOT NULL AUTO_INCREMENT,
MTG VARCHAR(60) NOT NULL,
AGE VARCHAR(60) NOT NULL,
REPORTER_EMAIL VARCHAR(60) NOT NULL,
REPORTER_ID VARCHAR(60) NOT NULL,
CENTRE_ID VARCHAR(60) NOT NULL,
REPORTER_NAME VARCHAR(60) NOT NULL,
CENTRE_NAME VARCHAR(60) NOT NULL,
UNIQUE_ID VARCHAR(60) NULL,
 PRIMARY KEY (ID)
);